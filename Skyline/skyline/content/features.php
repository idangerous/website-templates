<h2 class="window-heading">Skyline Features</h2>
<h3 class="inner-heading normal">Template Configuration</h3>
<p>Skyline has very powerful, flexible and easy to use API. It allows you to configure in &quot;One Click&quot; the following parameters:</p>
<ul>
<li>Disable/enable CSS3 transforms</li>
<li>Disable/enable Intro</li>
<li>Disable/enable some Fade effects for IE6 - IE8 browsers</li>
<li>You can set default window's color and toolbar style</li>
<li>Disable/enable windows rotation</li>
<li>Disable/enable scroll bar in windows</li>
<li>You can set duration for almost all animation effects, window open/expand/minimize durations, tooltip duration, portfolio slide duration, etc.</li>
<li>Configuration for intro slides - default slide duration, start and final slide's transformation</li>
<li>etc...</li>
</ul>
<h3 class="inner-heading normal">Skyline Windows</h3>
<p>Template API allows to call windows with predefined sizes, colors and toolbar styles. <a href="content/windows.php" class="new-window">Learn more ablout <strong>Window API</strong></a></p>
<h3 class="inner-heading normal">Zoom Slider</h3>
<p>Skyline comes with exclusive Zoom Slider. It is driven by simple HTML. All animation is realized with CSS3 transitions and jQuery. Zoom Slider has very flexible configuration which allows you to set animation time, rotate angles, scale multiplier, vertical and horizontal offsets in one click!. If browser doesn't support CSS3 transitions it will work like a simple fade to fade slider!</p>
<h3 class="inner-heading normal">Carousel Portfolio</h3>
<p>Skyline comes with a great looking animated carousel portfolio with a nice slide and scale effects. Its also driven by simple HTML, and you can put any HTML content into every portfolio slide!</p>
