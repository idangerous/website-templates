  <h3 class="cufoned slide-heading">Device - Desktop Style Template!</h3>
  <p style="text-align:justify"><strong>Device</strong> is a &quot;Desktop Style&quot; Ajax based animated website template. This template is perfect for small business websites, IT projects, or it can be used as a fashion site.</p>
<p style="text-align:justify">Page content is displayed in the windows that you can <strong>drag</strong>, <strong>resize</strong> and <strong>minimize</strong> (and later expand). You can put any content (flash, html, php, iframe etc.) inside the &quot;window&quot;. Due to the use of Ajax, your users do not need to wait the reloading of entire site. All content pages are loading &quot;on the fly&quot;.</p>
<p style="text-align:justify">Smooth and good looking animation and Ajax features of this template are realized with JavaScript jQuery and jQuery UI libraries.</p>
<p style="text-align:justify">Template does not have a typical site menu with a links. Instead menu, you can see grey rectangles in the center of home page and small square icons below. To open menu you need to click on a middle rectangle on the background image.</p>
