<h2 class="content-heading">&quot;Croplane&quot; Template Features</h2>
<p>The main feature of Croplane template is that it has five different content layouts:</p>
<ul>
<li>Simple windowed content like this page.</li>
<li><a href="javascript:goTo('slideshow',1)" title="Slideshow">Slideshow</a> layout type allows you to show content using animated slides.</li>
<li><a href="javascript:goTo('image_gallery',1)" title="Image Gallery">Image Gallery</a>. Show your photos in lightbox with a photo description.</li>
<li><a href="javascript:goTo('portfolio',1)" title="Portfolio">Portfolio</a>. Share your works in animated lightbox with a work description.</li>
<li><a href="javascript:goTo('offer',1)" title="Offer Section">Offer Section</a>. Actually you can put here not only images, but and any html content.</li>
<li></li>
</ul>
